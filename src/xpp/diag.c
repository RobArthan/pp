BOOL debug;

/* **** **** **** **** **** **** **** **** **** **** **** ****
 * msg: print a message to standard error
 * **** **** **** **** **** **** **** **** **** **** **** **** */

void msg(s1, s2)
char *s1, *s2;
{
	if(s2[strlen(s2) - 1] == '\n') {
		fprintf(stderr, "xpp: %s: %s", s1, s2);
	} else {
		fprintf(stderr, "xpp: %s: %s\n", s1, s2);
	};
	fflush(stderr);
}

/* **** **** **** **** **** **** **** **** **** **** **** ****
 * diag: print a message to standard error if debugging:
 * **** **** **** **** **** **** **** **** **** **** **** **** */

void diag(s1, s2)
char *s1, *s2;
{
	if (debug) {
		msg(s1, s2);
	};
}

